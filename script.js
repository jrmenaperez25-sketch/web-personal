const setText = (id, value) => {
  const node = document.getElementById(id);
  if (node) node.textContent = value;
};

const renderList = (id, items, mapper) => {
  const root = document.getElementById(id);
  if (!root) return;
  root.innerHTML = items.map(mapper).join("");
};

